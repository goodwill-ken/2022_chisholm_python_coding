# Calculate the area of a rectangle given user inputs for length and width
# Declare the type of input if prompt is a string

input_length = int (input('What is the length of the rectangle?'))
input_width = int (input('What is the width of the rectangle?'))

area = input_length * input_width

print('The area of a rectangle with a length of ', input_length, ' and a width of ', input_width, ' is', area)
